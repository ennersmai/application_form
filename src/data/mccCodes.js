// Function to intelligently categorize MCC codes
const categorizeMccCode = (description) => {
  const desc = description.toLowerCase()
  
  if (desc.includes('restaurant') || desc.includes('food') || desc.includes('eating') || desc.includes('bakery') || desc.includes('caterer') || desc.includes('drinking') || desc.includes('bar') || desc.includes('tavern') || desc.includes('fast food')) {
    return 'Food & Dining'
  } else if (desc.includes('store') || desc.includes('shop') || desc.includes('retail') || desc.includes('department') || desc.includes('supermarket') || desc.includes('grocery') || desc.includes('clothing') || desc.includes('apparel')) {
    return 'Retail'
  } else if (desc.includes('doctor') || desc.includes('physician') || desc.includes('dental') || desc.includes('medical') || desc.includes('hospital') || desc.includes('veterinary') || desc.includes('health') || desc.includes('optician') || desc.includes('pharmacy')) {
    return 'Healthcare'
  } else if (desc.includes('hotel') || desc.includes('motel') || desc.includes('resort') || desc.includes('lodging') || desc.includes('camp')) {
    return 'Hospitality'
  } else if (desc.includes('automotive') || desc.includes('car') || desc.includes('truck') || desc.includes('vehicle') || desc.includes('service station') || desc.includes('fuel') || desc.includes('tire') || desc.includes('auto')) {
    return 'Automotive'
  } else if (desc.includes('computer') || desc.includes('software') || desc.includes('programming') || desc.includes('data processing') || desc.includes('telecommunication') || desc.includes('digital')) {
    return 'Technology'
  } else if (desc.includes('contractor') || desc.includes('construction') || desc.includes('building') || desc.includes('electrical') || desc.includes('plumbing') || desc.includes('roofing') || desc.includes('carpentry')) {
    return 'Construction'
  } else if (desc.includes('entertainment') || desc.includes('theater') || desc.includes('movie') || desc.includes('amusement') || desc.includes('recreation') || desc.includes('sport') || desc.includes('gambling') || desc.includes('game')) {
    return 'Entertainment'
  } else if (desc.includes('transport') || desc.includes('airline') || desc.includes('travel') || desc.includes('taxi') || desc.includes('bus') || desc.includes('freight') || desc.includes('railroad')) {
    return 'Transportation'
  } else if (desc.includes('financial') || desc.includes('insurance') || desc.includes('bank') || desc.includes('real estate') || desc.includes('security broker')) {
    return 'Financial Services'
  } else if (desc.includes('service') || desc.includes('repair') || desc.includes('maintenance') || desc.includes('cleaning') || desc.includes('personal') || desc.includes('beauty') || desc.includes('barber')) {
    return 'Services'
  } else if (desc.includes('agricultural') || desc.includes('horticultural') || desc.includes('landscaping') || desc.includes('nursery')) {
    return 'Agriculture'
  } else if (desc.includes('publishing') || desc.includes('printing') || desc.includes('advertising') || desc.includes('photography')) {
    return 'Media & Publishing'
  } else {
    return 'Other'
  }
}

// Process the raw MCC codes into structured format
export const mccCodes = rawMccCodes.map(item => {
  const [code, ...descParts] = item.split(' - ')
  const description = descParts.join(' - ')
  return {
    code: code.trim(),
    description: description.trim(),
    group: categorizeMccCode(description),
    searchText: `${code.trim()} ${description.trim()}`.toLowerCase()
  }
})

// Get unique industry groups
export const industryGroups = [...new Set(mccCodes.map(mcc => mcc.group))].sort()
